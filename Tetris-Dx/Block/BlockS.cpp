#include "BlockS.h"

//--------------------------------------------------------------------------------------
BlockS::BlockS(int x, int y, int rot)
	:Block(x, y, rot, GetColor(5, 145, 255)) {
	initShape();
}
//--------------------------------------------------------------------------------------
void BlockS::initShape() {
	_shapeData[1][1] = 1;
	_shapeData[1][2] = 1;
	_shapeData[2][0] = 1;
	_shapeData[2][1] = 1;
}
