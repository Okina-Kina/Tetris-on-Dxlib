#include "BlockO.h"

//--------------------------------------------------------------------------------------
BlockO::BlockO(int x, int y, int rot)
	:Block(x, y, rot, GetColor(255, 255, 100)) {
	initShape();
}
//--------------------------------------------------------------------------------------
void BlockO::initShape() {
	_shapeData[1][1] = 1;
	_shapeData[1][2] = 1;
	_shapeData[2][1] = 1;
	_shapeData[2][2] = 1;
}
